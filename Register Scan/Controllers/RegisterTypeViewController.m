#import "RegisterTypeViewController.h"
#import "AttendanceViewController.h"

@interface RegisterTypeViewController ()
@property (nonatomic, retain) NSArray *types;
@end

@implementation RegisterTypeViewController

@synthesize moduleCode = _moduleCode;
@synthesize types = _types;

- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    self.title = self.moduleCode;
}

- (void) viewWillAppear:(BOOL)animated {
    self.types = [NSArray arrayWithObjects:@"Lecture", @"Lab 1", @"Lab 2", @"Lab 3", @"Lab 4", nil];
}

- (void) prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender {
    if ([segue.identifier isEqualToString:@"ShowAttendance"]) {
        AttendanceViewController *avc = segue.destinationViewController;
        NSIndexPath *selectedPath = [self.tableView indexPathForSelectedRow];
        NSString *type = [self.types objectAtIndex: selectedPath.row];
        avc.moduleCode = self.moduleCode;
        avc.type = type;
    }
}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.types.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    cell.textLabel.text = [self.types objectAtIndex:indexPath.row];
    return cell;
}

